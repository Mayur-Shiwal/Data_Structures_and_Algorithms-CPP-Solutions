class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        // code here 
        long long int ans = 0, sum=0;
        int i=0, j=0;
        while(i<N && j<N){
            if(j-i >= K){
                sum+=Arr[j++];
                sum-=Arr[i++];
            }
            else sum += Arr[j++];
            ans = max(ans, sum);
        }
        return ans;
    }
};

